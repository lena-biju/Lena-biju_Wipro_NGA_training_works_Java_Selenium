package Day1_to_2;

public class Assignmentoperator {
	public static void main(String[]args) {
		int a=10;
		a+=5;//a=a+5
		System.out.println(a);
		
		
		int b=15;
		b-=5;//b=b-5
		System.out.println(b);
		
		//similar like these we can use   *,/
		
		
		
		
		//ternary operator ? :
		
		int m=35;
		String result;
		result=(m>25)?"passed":"failed";
		System.out.println(result);
	}

}
